package com.example.android.loonatheapp;

public class Member {
    private String name;
    private String hangul;
    private int photo;
    private int color;
    private String date;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getHangul() {
        return hangul;
    }

    public void setHangul(String hangul) {
        this.hangul = hangul;
    }

    public int getPhoto() {
        return photo;
    }

    public void setPhoto(int photo) {
        this.photo = photo;
    }

    public int getColor() {
        return color;
    }

    public void setColor(int color) {
        this.color = color;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
